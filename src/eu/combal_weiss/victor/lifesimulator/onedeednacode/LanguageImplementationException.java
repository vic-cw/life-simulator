
package eu.combal_weiss.victor.lifesimulator.onedeednacode;

public class LanguageImplementationException extends Exception {

    /**
     * Creates a new instance of <code>LanguageImplementationException</code> without detail message.
     */
    public LanguageImplementationException() {
    }

    /**
     * Constructs an instance of <code>LanguageImplementationException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public LanguageImplementationException(String msg) {
        super(msg);
    }
}
